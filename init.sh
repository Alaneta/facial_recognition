#!/bin/bash

echo "###########################################################################################"
echo "Instalando Virtual Environment"
echo "###########################################################################################"

pip install virtualenv
virtualenv venv

echo "###########################################################################################"
echo "Activando Virtual Environment"
echo "###########################################################################################"

source venv/bin/activate

echo "###########################################################################################"
echo "Instalando dependencias de requirements.txt"
echo "###########################################################################################"

pip install -r requirements.txt
